    public void register() throws IOException{
        if(!validator()){
            //read_database();
            if(search_database()){
                JOptionPane.showMessageDialog(null, "N.I.F. "+text_field_nif.getText()+" is already on our database");
            } 
            else{
                if(search_login()){
                    JOptionPane.showMessageDialog(null, "Login "+text_field_login.getText()+" is already on our database");
                }
                else{
                    if(!nif_validator()){
                        JOptionPane.showMessageDialog(null, "N.I.F. format is not valid, a valid format is: \n"
                                + "11111111A");
                    }
                    else{
                       if(!password_isValid()){
                        JOptionPane.showMessageDialog(null, "The password must have 8 characters, almost one Mayus, \none number "
                                + "and one of this symbols: \n"
                                + "?, ¿, !, ¡, -, &, +, *");
                        }
                        else{
                            if(!password_validator()){
                                JOptionPane.showMessageDialog(null, "The passwords must be the same");
                            }
                            else{
                                if(!spinner_validator()){
                                    JOptionPane.showMessageDialog(null, "The age is not valid");
                                }
                                else{
                                    JOptionPane.showMessageDialog(null, "All the fields must be filled");
                                }
                            }
                        } 
                    }
                }
            }
        }
        else{
            write_database();
            cleanFields();
        }
        
}
    
