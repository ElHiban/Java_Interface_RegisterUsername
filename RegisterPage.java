    public boolean search_login() throws FileNotFoundException, IOException{
        File searchDB = new File("database");

		BufferedReader DBreader = new BufferedReader(new FileReader(searchDB));
		
		String loginToFind = text_field_login.getText();
		String currentLine;
		
		boolean was_found=false;

		while((currentLine = DBreader.readLine()) != null) {
		    // trim newline when comparing with lineToRemove
			String[]all_DB =currentLine.split("_");
                        String login_DB = all_DB[4];
		    if(login_DB.equals(loginToFind)) {
		    	was_found=true;
		    }
		}
		DBreader.close(); 
        return was_found;
    }
    
    public boolean search_database() throws FileNotFoundException, IOException{
        File searchDB = new File("database");

		BufferedReader DBreader = new BufferedReader(new FileReader(searchDB));
		
		String nifToFind = text_field_nif.getText();
		nifToFind=nifToFind.toUpperCase();
		String currentLine;
		
		boolean was_found=false;

		while((currentLine = DBreader.readLine()) != null) {
		    // trim newline when comparing with lineToRemove
			String[]all_DB =currentLine.split("_");
                        String nif_DB = all_DB[3];
		    if(nif_DB.equals(nifToFind)) {
		    	was_found=true;
		    }
		}
		DBreader.close(); 
        return was_found;
    }
    
    public boolean validator(){
        boolean validation=false;
        
        if(!text_field_name.getText().equals("") 
                && !text_field_lastname.getText().equals("")
                && !text_field_nif.getText().equals("")
                && nif_validator()
                && spinner_validator()
                && !text_field_login.getText().equals("")
                && !password_field_password.getText().equals("")
                && !password_field_confirmation.getText().equals("")
                && password_validator()
                && password_isValid()){
            validation=true;
        }
        
        return validation;
    }
    
    public boolean nif_validator(){
        boolean nif_is=false;
        
        if(text_field_nif.getText().length()!=9 || !nif_numbers() || Character.isLetter(text_field_nif.getText().charAt(8))==false){
            nif_is=false;
        }
        else{
            nif_is=true;
        }
        return nif_is;
    }
    
    public boolean nif_numbers(){
        boolean nif_number=false;
        int cont_numbers=0;
        
        for(int i=0; i<((text_field_nif.getText().length())-1); i++){
            char c = text_field_nif.getText().charAt(i);
            if(c=='0'|| c=='1'|| c=='2'|| c=='3'|| c=='4'|| c=='5'|| c=='6'|| c=='7'|| c=='8'|| c=='9'){
                cont_numbers++;
            }
        }
        if(cont_numbers==8){
            nif_number=true;
        }
        
        return nif_number;
    }
    
    public boolean spinner_validator(){
        boolean sp_validator=false;
        if(((int) spinner_age.getValue())<=120 && ((int) spinner_age.getValue())>=18){
            sp_validator=true;
        }
        return sp_validator;
    }
    
    public boolean password_validator(){
        boolean pw_validator=false;
        if(password_field_password.getText().equals(password_field_confirmation.getText())){
            pw_validator=true;
        }
        return pw_validator;
    }
    
    public boolean password_isValid(){
        boolean isValid=false;
        if(password_field_password.getText().length()<8){
            isValid=false;
        }
        if(hasMayus()){
            if(hasNumbers()){
                if(hasSymbols()){
                    isValid=true;
                }
            }
        }
        if(!hasBarraBaja()){
            isValid=false;
        }
        return isValid;
    }
           
    public boolean hasBarraBaja(){
        for(int i=0; i<(password_field_password.getText().length()); i++){
            char c = password_field_password.getText().charAt(i);
            if(c=='_'){
                return false;
            }
        }
        return true;
    }
    
    public boolean hasMayus(){
        for(int i=0; i<(password_field_password.getText().length()); i++){
            char c = password_field_password.getText().charAt(i);
            if(c=='A'|| c=='B'|| c=='C' ||c=='D'|| c=='E'|| c=='F'|| c=='G'|| c=='H'|| c=='I'|| c=='J'|| c=='K'|| c=='L'|| c=='M'
                    || c=='N'|| c=='Ñ'|| c=='O'|| c=='P'|| c=='Q'|| c=='R'|| c=='S'|| c=='T'|| c=='U'|| c=='V'|| c=='W'|| c=='X'
                    || c=='Y'|| c=='Z'){
                return true;
            }
        }
        return false;
    }
    
    public boolean hasNumbers(){
        for(int i=0; i<(password_field_password.getText().length()); i++){
            char c = password_field_password.getText().charAt(i);
            if(c=='0'|| c=='1'|| c=='2'|| c=='3'|| c=='4'|| c=='5'|| c=='6'|| c=='7'|| c=='8'|| c=='9'){
                return true;
            }
        }
        return false;
    }
    
    public boolean hasSymbols(){
        for(int i=0; i<(password_field_password.getText().length()); i++){
            char c = password_field_password.getText().charAt(i);
            if(c=='?'|| c=='¿'|| c=='!'|| c=='¡'|| c=='-'|| c=='&'|| c=='+'|| c=='*'){
                return true;
            }
        }
        return false;
    }
    
