    public void write_database(){
        FileWriter database = null;
        PrintWriter pw = null;
        try{
            database = new FileWriter("database", true);
            pw = new PrintWriter(database);
            pw.print(newLine());	
        } catch (Exception e) {
            e.printStackTrace();
        } finally {
           try {
           if (null != database)
              database.close();
           } catch (Exception e2) {
              e2.printStackTrace();
           }
        }
    }
    
